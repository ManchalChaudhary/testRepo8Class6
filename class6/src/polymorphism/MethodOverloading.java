package polymorphism;

public class MethodOverloading {

	public static void main(String[] args) {
		
		
	}
	// method overloading: polymorphism	
	public static void test () {System.out.println("Hello");}
	public static void test (int a) {System.out.println("Hello");}
	public static void test (int a, int b) {System.out.println("Hello");}
	public static void test (int a, int b, int  c) {System.out.println("Hello");}
	public static void test (String a) {System.out.println("Hello");}
	public static void test (String a, String b) {System.out.println("Hello");}
	public static void test (String a, int b) {System.out.println("Hello");}
	public static void test (int a, String b) {System.out.println("Hello");}
	
	
}
